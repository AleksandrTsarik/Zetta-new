    const handleScroll = () => {
      const scrolled = window.scrollY > 400;

      if (scrolled !== showButton.value) {
        showButton.value = scrolled;
        if (btnUpRef.value) {
          if (scrolled) {
            btnUpRef.value.classList.add("active");
